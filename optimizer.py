def RangerLars(params, alpha=0.5, k=6, *args, **kwargs):
    '''
    lookahead with RaLAMB as inner optimizer
    work with only O(N* adam's) learning rate
    '''
    ralamb = Ralamb(params, *args, **kwargs)
    return Lookahead(ralamb, alpha, k)
 
def Over9000(params, alpha=0.5, k=6, *args, **kwargs):
    '''
    lookahead with RaLAMB as inner optimizer
    work with O(N* adam's) learning rate, so just a bit higher
    '''
    ralamb = Ralamb(params, *args, **kwargs)
    return Lookahead(ralamb, alpha, k)
 
def Ranger(params, alpha=0.5, k=6, *args, **kwargs):
    '''
    lookahead with RAdam as inner optimizer
    '''
    ralamb = RAdam(params, *args, **kwargs)
    return Lookahead(ralamb, alpha, k)